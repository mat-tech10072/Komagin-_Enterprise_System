        csvRow([]);
    }

    // Footer for full export
    if ($type === 'all') {
        csvRow([]);
        csvRow(['END OF REPORT — ' . $companyName]);
        csvRow(['This document is for legal reference and internal use only.']);
        csvRow(['Generated at', $generatedAt, 'by', $generatedBy]);
        exit;
    }
}

