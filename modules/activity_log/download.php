// ── Helper: send CSV headers ───────────────────────────────────────────────
function csvStart(string $filename): void {
    header('Content-Type: text/csv; charset=UTF-8');
    header('Content-Disposition: attachment; filename="' . $filename . '"');
    header('Cache-Control: no-cache, no-store, must-revalidate');
    echo "\xEF\xBB\xBF"; // UTF-8 BOM for Excel
}

function csvRow(array $cols): void {
    $escaped = array_map(function($v) {
        $v = str_replace('"', '""', (string)$v);
        return '"' . $v . '"';
    }, $cols);
    echo implode(',', $escaped) . "\r\n";
}

function csvMeta(string $company, string $title, string $from, string $to, string $by, string $at): void {
    csvRow([$company . ' — ' . $title]);
    csvRow(['Generated by', $by]);
    csvRow(['Generated at', $at]);
    csvRow(['Date range', $from . ' to ' . $to]);
    csvRow([]);
}

