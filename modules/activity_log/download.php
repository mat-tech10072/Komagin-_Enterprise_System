            $filename = 'activity_all_consultants_' . date('Ymd') . '.csv';
            csvStart($filename);
            csvMeta($companyName, 'Activity Log — All Consultants', $dateFrom, $dateTo, $generatedBy, $generatedAt);
        }

        csvRow(['═══ CONSULTANTS ═══']);
        csvRow(['#','Date/Time','Consultant Name','Consultant Number','Type','Work Date','Clock In','Break Out','Break In','Clock Out','Total Hours','Notes']);

        $stmt = db()->prepare("SELECT ca.*, c.first_name, c.last_name, c.consultant_number, c.type
            FROM consultant_attendance ca
            JOIN consultants c ON c.id = ca.consultant_id
            WHERE ca.work_date BETWEEN ? AND ?
            ORDER BY c.last_name ASC, ca.work_date DESC");
        $stmt->execute([$dateFrom, $dateTo]);
        $i = 1;
        while ($log = $stmt->fetch(PDO::FETCH_ASSOC)) {
            csvRow([
                $i++,
                $log['created_at'],
                trim($log['first_name'] . ' ' . $log['last_name']),
                $log['consultant_number'],
                $log['type'] === 'time_based' ? 'Time-Based' : 'Output-Based',
                $log['work_date'],
                $log['clock_in']    ? date('Y-m-d H:i:s', strtotime($log['clock_in']))    : '',
                $log['break_start'] ? date('Y-m-d H:i:s', strtotime($log['break_start'])) : '',
                $log['break_end']   ? date('Y-m-d H:i:s', strtotime($log['break_end']))   : '',
                $log['clock_out']   ? date('Y-m-d H:i:s', strtotime($log['clock_out']))   : '',
                $log['total_hours'] !== null ? number_format((float)$log['total_hours'], 2) : '',
                $log['notes'] ?? '',
