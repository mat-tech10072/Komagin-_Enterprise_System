                $te['portal_active'] ? 'Yes' : 'No',
                $te['portal_last_login'] ?? 'Never',
                $te['notes'] ?? '',
                $te['created_at'],
            ]);
        }
        if ($type === 'temp') { exit; }
        csvRow([]);
